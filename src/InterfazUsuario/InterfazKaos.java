    private void discover(java.awt.event.ActionEvent e) {
        String emocion = ((JMenuItem) e.getSource()).getText();
        Object[] args = {emocion};
        try {
            ac = cc.createNewAgent("Planeador", "Agentes.AgPlaneador", args);
            ac.start();
        } catch (StaleProxyException ex) {
            Logger.getLogger(InterfazKaos.class.getName()).log(Level.SEVERE, null, ex);
        }
        showStatus("Explorando obras para " + emocion);
        TextFieldArchivoLorenz.setText("Sugerencia para " + emocion);
    }

